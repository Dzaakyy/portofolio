<script>
export default {
  data() {
    return {
      form: {
        nama: '',
        email: '',
        subjek: '',
        pesan: ''
      },
      errors: {},
      isSubmitting: false,
      showSuccessMessage: false
    }
  },
  methods: {
    validateForm() {
      this.errors = {};

      if (!this.form.nama) {
        this.errors.nama = 'Nama lengkap harus diisi';
      }

      if (!this.form.email) {
        this.errors.email = 'Email harus diisi';
      } else if (!this.validEmail(this.form.email)) {
        this.errors.email = 'Format email tidak valid';
      }

      if (!this.form.subjek) {
        this.errors.subjek = 'Subjek harus diisi';
      }

      if (!this.form.pesan) {
        this.errors.pesan = 'Pesan harus diisi';
      } else if (this.form.pesan.length < 10) {
        this.errors.pesan = 'Pesan minimal 10 karakter';
      }

      return Object.keys(this.errors).length === 0;
    },
    validEmail(email) {
      const re = /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
      return re.test(email);
    },
    submitForm() {
      if (this.validateForm()) {
        this.isSubmitting = true;

        setTimeout(() => {
          this.isSubmitting = false;
          this.showSuccessMessage = true;

          this.form = {
            nama: '',
            email: '',
            subjek: '',
            pesan: ''
          };

          setTimeout(() => {
            this.showSuccessMessage = false;
          }, 5000);
        }, 1500);
      }
    }
  }
}
